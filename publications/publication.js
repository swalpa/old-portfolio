function showPapers(papersContainer, papersData) {
    var paperContent = ''
    for (let i = 0; i < papersData.length; i++) {
        paperContent += `<div   itemscope itemtype="http://schema.org/CreativeWork">
    <i class="fa fa-file-text-o pub-icon" aria-hidden="true"></i>
    <a href=${papersData[i].paperLink}  target="_blank" itemprop="name">
     ${papersData[i].name}</a>
    <span itemprop="author">
     ${papersData[i].author}
    </span>
    ${papersData[i].publisherName}
    <p>`
        if (papersData[i].arXivLink !== "") {
            paperContent += `
        <a class="btn btn-primary btn-outline btn-sm" href=${papersData[i].arXivLink} target="_blank"
          rel="noopener">
          arXiv
        </a>`

        }
        if (papersData[i].pdfLink !== "") {
            paperContent += `
        <a class="btn btn-primary btn-outline btn-sm" href=${papersData[i].pdfLink} target="_blank"
          rel="noopener">
          PDF
        </a>`

        }
        if (papersData[i].codeLink !== "") {
            paperContent += `
        <a class="btn btn-primary btn-outline btn-sm" href=${papersData[i].codeLink}
          target="_blank" rel="noopener">
          Code
        </a>
       </p>
       </div>  `

        }

    }
    papersContainer.insertAdjacentHTML("beforeend", paperContent);
}


